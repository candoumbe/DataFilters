namespace DataFilters
{
    /// <summary>
    /// Enumeration of direction that can be associated to a <see cref="Sort{T}"/> instance
    /// </summary>
    public enum SortDirection : short
    {
        /// <summary>
        /// Order elements from the lowest to the highest.
        /// </summary>
        Ascending,
        /// <summary>
        /// Order eleemnts from the highest to the lowest.
        /// </summary>
        Descending
    }
}
